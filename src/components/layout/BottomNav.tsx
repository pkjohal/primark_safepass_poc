export default function BottomNav() {
  const { user } = useAuth()
  if (!user) return null

  return (
    <nav className="bg-white border-t border-border-grey flex sm:hidden shrink-0 no-print">
      {navItems
        .filter((item) => {
          if (!item.minRole) return true
          return user.role === item.minRole
        })
        .map((item) => (
          <NavLink
            key={item.to}
            to={item.to}
            end={item.to === '/'}
            className={({ isActive }) =>
              `flex-1 flex flex-col items-center justify-center py-2 gap-0.5 text-xs font-medium transition-colors ${
                isActive ? 'text-primark-blue' : 'text-mid-grey'
              }`
            }
          >
            {item.icon}
            {item.label}
          </NavLink>
        ))}
    </nav>
  )
}
